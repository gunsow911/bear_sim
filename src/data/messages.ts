/**
 * そのターン開始時に表示すべき定型メッセージを返す（なければ null）。
 * 10月入り（turn 9）に晩秋の警告を一度だけ出す。
 */
export function seasonalMessageForTurn(turn: number): GameMessage | null {
  if (turn === 9) return AUTUMN_WARNING
  return null
}
